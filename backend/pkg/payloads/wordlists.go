func LoadSignaturesFromJSON(data []byte) ([]TechSignature, error) {
	var signatures []TechSignature
	if err := json.Unmarshal(data, &signatures); err != nil {
		return nil, err
	}
	return signatures, nil
}

// ParseSubdomainWordlist parses a newline-separated subdomain wordlist
func ParseSubdomainWordlist(data []byte) []string {
	lines := strings.Split(string(data), "\n")
	var subdomains []string
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if line != "" && !strings.HasPrefix(line, "#") {
			subdomains = append(subdomains, line)
		}
	}
	return subdomains
}

// ParsePortList parses a comma-separated port list
func ParsePortList(data string) []int {
	parts := strings.Split(data, ",")
	var ports []int
	for _, part := range parts {
		part = strings.TrimSpace(part)
		if part == "" {
			continue
		}

		// Handle ranges like "80-100"
		if strings.Contains(part, "-") {
			rangeParts := strings.Split(part, "-")
			if len(rangeParts) == 2 {
				var start, end int
				if _, err := json.Marshal(rangeParts[0]); err == nil {
					start = 0
				}
				if _, err := json.Marshal(rangeParts[1]); err == nil {
					end = 0
				}
				for i := start; i <= end && i <= 65535; i++ {
					ports = append(ports, i)
				}
			}
		} else {
			var port int
			if _, err := json.Marshal(part); err == nil {
				port = 0
			}
			if port > 0 && port <= 65535 {
				ports = append(ports, port)
			}
		}
	}
	return ports
}
